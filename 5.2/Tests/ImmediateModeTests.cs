using ConsoleApp;
using Xunit;

namespace Tests
{
    public class ImmediateModeTests
    {
        [Theory]
        //Addition
        [InlineData("1101,100,-1,4,0", 99)]
        //Multiplication
        [InlineData("1002,4,3,4,33", 99)]
        public void WritesExpected(string inputString, int expected)
        {
            var intCode = new Intcode();
            intCode.Process(inputString);

            Assert.Equal(expected, intCode.DumpMemory()[4]);
        }

        [Theory]
        //Equals
        [InlineData("3,3,1108,-1,8,3,4,3,99", 8, 1)]
        [InlineData("3,3,1108,-1,8,3,4,3,99", 9, 0)]
        [InlineData("3,3,1108,-1,8,3,4,3,99", 1, 0)]
        [InlineData("3,3,1108,-1,8,3,4,3,99", 0, 0)]
        [InlineData("3,3,1108,-1,8,3,4,3,99", -1, 0)]
        //Greater than
        [InlineData("3,3,1107,-1,8,3,4,3,99", 8, 0)]
        [InlineData("3,3,1107,-1,8,3,4,3,99", 9, 0)]
        [InlineData("3,3,1107,-1,8,3,4,3,99", 100, 0)]
        [InlineData("3,3,1107,-1,8,3,4,3,99", 7, 1)]
        [InlineData("3,3,1107,-1,8,3,4,3,99", 1, 1)]
        [InlineData("3,3,1107,-1,8,3,4,3,99", 0, 1)]
        [InlineData("3,3,1107,-1,8,3,4,3,99", -1, 1)]
        //Zero if zero
        [InlineData("3,3,1105,-1,9,1101,0,0,12,4,12,99,1", 0, 0)]
        //One if one
        [InlineData("3,3,1105,-1,9,1101,0,0,12,4,12,99,1", 1, 1)]
        //999 if less than 8
        [InlineData("3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99", 7, 999)]
        [InlineData("3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99", 1, 999)]
        [InlineData("3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99", 0, 999)]
        [InlineData("3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99", -1, 999)]
        //1000 if 8
        [InlineData("3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99", 8, 1000)]
        //1001 if greater than 8
        [InlineData("3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99", 9, 1001)]
        [InlineData("3,21,1008,21,8,20,1005,20,22,107,8,21,20,1006,20,31,1106,0,36,98,0,0,1002,21,125,20,4,20,1105,1,46,104,999,1105,1,46,1101,1000,1,20,4,20,1105,1,46,98,99", 100, 1001)]
        public void EmitsExpected(string inputString, int inputValue, int expected)
        {
            var intCode = new Intcode(() => { return inputValue; }, (output) =>
            {
                Assert.Equal(expected, output);
            });

            intCode.Process(inputString);
        }
    }
